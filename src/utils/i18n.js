const LANGUAGE_KEY = 'agentFootLanguage';

const getDeviceLanguage = () => {
  const language = navigator.languages?.[0] || navigator.language || 'fr';
  return language.toLowerCase().startsWith('fr') ? 'fr' : 'en';
};

export const getAppLanguage = () => localStorage.getItem(LANGUAGE_KEY) || getDeviceLanguage();

export const setAppLanguage = (language) => {
  const safeLanguage = language === 'en' ? 'en' : 'fr';
  localStorage.setItem(LANGUAGE_KEY, safeLanguage);
  document.documentElement.lang = safeLanguage;
  window.location.reload();
};

export const isEnglish = () => getAppLanguage() === 'en';

